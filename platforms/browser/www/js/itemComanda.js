function ItemComanda(id, comanda, item, codigoInterno, codigoEAN, descricao, codigoVendedor, tipoPreco, quantidade, precoUnitario, percentualDesconto, percentualAcrescimo, descontMultiplo, complemento, cancelado, valorDesconto, valorAcrescimo, impresso){
	this.id = id
	this.comanda = comanda;
	this.item = item;
	this.codigoInterno = codigoInterno;
	this.codigoEAN = codigoEAN;
	this.descricao = descricao;
	this.codigoVendedor = codigoVendedor;
	this.tipoPreco = tipoPreco;
	this.quantidade = quantidade;
	this.precoUnitario = precoUnitario;
	this.percentualDesconto = percentualDesconto;
	this.percentualAcrescimo = percentualAcrescimo;
	this.descontMultiplo = descontMultiplo;
	this.complemento = complemento;
	this.cancelado = cancelado;
	this.valorDesconto = valorDesconto;
	this.valorAcrescimo = valorAcrescimo;
	this.impresso = impresso;
}
